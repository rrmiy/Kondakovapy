#Дан список A размера N. Сформировать два новых списка B и C: в список B записать все положительные элементы список A, в список C — все отрицательные (сохраняя исходный порядок следования элементов). Вывести вначале размер и содержимое списка B, а затем — размер и содержимое списка C.

# Исходный список A
A = [-1, 4, -7, 8, 0, -9, 52]
N = len(A)

# Создаем пустые списки B и C
B = []
C = []

# Проходим по каждому элементу списка A
for element in A:
    if element > 0:
        # Если элемент положительный, добавляем его в список B
        B.append(element)
    elif element < 0:
        # Если элемент отрицательный, добавляем его в список C
        C.append(element)

# Выводим результаты
print(f'Размер списка B: {len(B)}')
print('Элементы списка B:', *B)

print(f'Размер списка C: {len(C)}')
print('Элементы списка C:', *C)